int	ft_sqrt(int nb)
{
	long	kok;
	long	b;

	b = nb;
	if (b <= 0)
		return (0);
	if (b == 1)
		return (1);
	kok = 2;
	if (b >= 2)
	{
		while (kok * kok <= b)
		{
			if (kok * kok == b)
			{
				return (kok);
			}
			kok++;
		}
	}
	return (0);
}
